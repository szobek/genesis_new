                <p align="justify"><b>Mi a cookie (süti)?</b></p>
                <p align="justify">A cookie egy kisméretű fájl, mely csak betűket és számokat tartalmaz, és ami egy
                    felhasználó számítógépén, mobiltelefonján vagy egyéb, Internetes hozzáférést biztosító eszközén
                    tárolható. A cookie egy információcsomag, amelyet az adott webszerver küld a böngészőnek, majd a
                    böngésző visszaküld a szervernek minden, a szerver felé irányított kérés alkalmával. A sütik
                    „passzívak”, azaz nem tartalmaznak futtatható állományokat, vírusokat, illetve kémprogramokat
                    (spyware), valamint nem férnek hozzá a felhasználó merevlemezének adataihoz.</p>
                <p align="justify">A sütik lehetővé teszik a felhasználó Internetezésre használt eszközének
                    felismerését, és ezáltal releváns, a felhasználó igényeihez igazított tartalom megjelenítését. A
                    sütik a felhasználó számára egyszerűbb böngészést biztosítanak, és elősegítik, hogy a társaságunk a
                    felhasználó számára minél kényelmesebb szolgáltatásokat nyújtson, úgy mint az online
                    adatbiztonsággal kapcsolatos igények vagy a releváns reklámok. Mindemellett anonim statisztikák
                    elkészítéséhez is használhatóak, melyek értékes információkkal szolgálnak társaságunknak a
                    felhasználók oldalhasználati szokásaival kapcsolatosan. A kapott statisztikai eredményeket
                    felhasználva tudjuk az oldal szerkezetét és tartalmát az igényeknek megfelelően alakítani, mindezt a
                    felhasználók anonimitásának megőrzése mellett.</p>
