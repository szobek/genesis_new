                <p align="justify">Többségében a sütik hatékony működéséhez nem szükségesek a személyes adatok, így az
                    esetek döntő többségében a cookie nem azonosítják az adott felhasználót. A cookie-k használata során
                    összegyűjtött személyes adatok kizárólag olyan célból használhatók fel, hogy bizonyos funkciók
                    használatát megkönnyítsék a felhasználó számára. Ezek az adatok olyan módon vannak tárolva, hogy
                    azokhoz jogosulatlan személyek nem férhetnek hozzá.</p>
